# Kérjük be az első számot a felhasználótól
szam1 = float(input("Kérlek, adj meg az első számot: "))

# Kérjük be a második számot a felhasználótól
szam2 = float(input("Kérlek, adj meg a második számot: "))

# Összeadjuk a két számot
eredmeny = szam1 + szam2

# Megjelenítjük az eredményt
print("A két szám összege:", eredmeny)